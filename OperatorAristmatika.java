public class OperatorAristmatika {
    public static void main (String[]args ){
    //Deklarasi dan inisiasi variable
    
    int i =9;
    int j =4;
    
    //menampilkan nilai varible
    System.out.println("nilai variabel :");
    System.out.println("i =:"+i);
    System.out.println("j =:"+j);
    
    //menggunakan operator penjumlahan
    System.out.println("Penjulahan :");
    System.out.println("i+j =:"+(i+j));
   
    //menggunakan operator pengurangan
    System.out.println("Pengurangan:");
    System.out.println("i-j =:"+(i-j));
    
    //menggunakan operator perkalian
    System.out.println("Perkalian :");
    System.out.println("i*j =:"+(i*j));
    
    //menggunakan operator pembagian
    System.out.println("Pembagian :");
    System.out.println("i/j =:"+(i/j));
    
    //menggunakan operator modulus
    System.out.println("Penjulahan :");
    System.out.println("i%j =:"+(i%j));
}
}

